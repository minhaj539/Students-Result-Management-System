    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        
        String rollno=jTextField1.getText();
        String physics=jTextField2.getText();
        String chem=jTextField3.getText();
        String maths=jTextField4.getText();
        String DBMS=jTextField5.getText();
        String OS=jTextField6.getText();
        
        try{
             Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","Minh@j539");
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery("select * from student where rollno='"+rollno+"'");
            if(rs.next()){
            st.executeUpdate("insert into result(rollno,physics,chem,maths,DBMS,OS) values('"+rollno+"','"+physics+"','"+chem+"','"+maths+"','"+DBMS+"','"+OS+"')");
            JOptionPane.showMessageDialog(null,"result updated successfully");
            setVisible(false);
            new insertnewresult().setVisible(true);
            }
            else{
                JOptionPane.showMessageDialog(null,"Rollno does not exist");
            }
       
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e.toString());
        }
        
        
    }//GEN-LAST:event_jButton6ActionPerformed

